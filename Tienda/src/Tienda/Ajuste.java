    public boolean contraseña(JFrame Frame) {

        try {
            usuario = opera.conseguir_color(1);
        } catch (SQLException ex) {
            Logger.getLogger(Ajuste.class.getName()).log(Level.SEVERE, null, ex);
        }
        String contraseña = cmensaje.mensaje_de_pregunta("Ingrese contraseña:");
        if (contraseña == null || contraseña.isEmpty()) {
            cmensaje.mensaje_error("Accion cancelada por el usuario");
            return false; // Salir del método si la contraseña es nula o vacía
        }

        if (contraseña.equals(usuario) && contraseña != null) {
            Frame.setVisible(true);
            dispose();
            return true;
        } else {

            cmensaje.mensaje_error("Error contraseña incorrecta");
        return false;
        }
    }

