    public Color mandarfechaycolores(int numero) {
        LocalDate fechaActual = LocalDate.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

        String color = null;

        try {
            color = opera.conseguir_color(numero);
        } catch (SQLException ex) {
            Logger.getLogger(Ajuste.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[] valores = color.split(",");
        int r = Integer.parseInt(valores[0]); // Componente rojo
        int g = Integer.parseInt(valores[1]); // Componente verde
        int b = Integer.parseInt(valores[2]); // Componente azul
        // Crear el color
        Color retornacolor = new Color(r, g, b);

        // Formatear la fecha
        String fecha = fechaActual.format(formato);
        jLFecha.setText(fecha + "");
        return retornacolor;

    }

    public void colorear(Color color1, Color color2, String actualiza, JPanel panel) throws SQLException {
        // SELECCIONAR COLOR 1

        color1 = Color.BLACK;

        color1 = JColorChooser.showDialog(this, "SELECIONA UN COLOR", color1);
        if (color1 == null) {
            color1 = color2;
        }
        //this.color1 = color1;
        panel.setBackground(color1);
        int r = color1.getRed();
        int g = color1.getGreen();
        int b = color1.getBlue();

        // Convertir a formato "R,G,B"
        String colorEnTexto = "'" + r + "," + g + "," + b + "'";
        opera.Actualizar(1, "Usuario", actualiza, colorEnTexto);

        PanelOpcionColor = mandarfechaycolores(2);
        FondoOpcionColor = mandarfechaycolores(3);
        jPBARRA.setBackground(PanelOpcionColor);
        jPFONDO.setBackground(FondoOpcionColor);
        jPanelseleccion.setBackground(FondoOpcionColor);
        jPanel1.setBackground(PanelOpcionColor);

        
    }

