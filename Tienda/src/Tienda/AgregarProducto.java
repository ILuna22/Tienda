    private void jBBarraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBarraActionPerformed

        if (cmensaje.mensaje_confirmacion("Desea a침adir el producto?", "Procesando...", "Error, elemento desechado")) {
            //comparar elementos
            boolean opcion = false;
            String lista[] = new String[6];
            for (int i = 0; i < productos.size(); i++) {
                String fila[] = productos.get(i);
                if (fila[0].equals(jTextCodigo.getText()) || fila[1].equals(jTextNombre.getText())) {
                    cmensaje.mensaje_error("Valores duplicados, revise codigo o nombre");
                    opcion = false;
                    break;
                } else {
                    if (jTextPCompra.getText().equals(jTextPVenta.getText())) {
                        cmensaje.mensaje_error("El precio de compra no puede ser mayor ni igual al de venta");
                        opcion = false;
                        break;
                    } else {
                        //armar lista
                        lista[0] = jTextCodigo.getText();
                        lista[1] = jTextNombre.getText();
                        lista[2] = jTextPVenta.getText();
                        lista[3] = jTextPCompra.getText();
                        try {
                            lista[4] = "" + inventario.obtener_indice(categoria, jComboBoxCategoria);
                            lista[5] = "" + inventario.obtener_indice(provedor, jComboBoxProveedor);
                            opcion = true;
                        } catch (SQLException ex) {
                            Logger.getLogger(AgregarProducto.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                }
            }
            try {
                if (opcion) {
                    opera.Insertar_Producto(lista);
                    jTextCodigo.setText("");
                    jTextNombre.setText("");
                    jTextPCompra.setText("");
                    jTextPVenta.setText("");
                    cmensaje.mensaje("Producto a침adido con exito");

                }
            } catch (SQLException ex) {
                Logger.getLogger(AgregarProducto.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jBBarraActionPerformed

